class ТранспортнийЗасіб:
    def __init__(self, назва, швидкість=0):
        self.назва = назва
        self.швидкість = швидкість
    
    def переміщення(self, відстань):
        час = відстань / self.швидкість if self.швидкість > 0 else 0
        return f"{self.назва} подолає {відстань} км за {час:.2f} годин" if час > 0 else f"{self.назва} не рухається"
    
    def __str__(self):
        return f"{self.назва} (швидкість: {self.швидкість} км/год)"


class Автомобіль(ТранспортнийЗасіб):
    def __init__(self, назва, швидкість, паливо):
        super().__init__(назва, швидкість)
        self.паливо = паливо
    
    def переміщення(self, відстань):
        базовий_результат = super().переміщення(відстань)
        return f"{базовий_результат}, використовує {self.паливо}"


class Літак(ТранспортнийЗасіб):
    def __init__(self, назва, швидкість, висота_польоту):
        super().__init__(назва, швидкість)
        self.висота_польоту = висота_польоту
    
    def переміщення(self, відстань):
        return f"{super().переміщення(відстань)} на висоті {self.висота_польоту} м"


class Велосипед(ТранспортнийЗасіб):
    def __init__(self, назва, швидкість, тип):
        super().__init__(назва, швидкість)
        self.тип = тип
    
    def переміщення(self, відстань):
        return f"{super().переміщення(відстань)} (тип велосипеда: {self.тип})"


if __name__ == "__main__":
    авто = Автомобіль("Toyota Camry", 120, "бензин")
    літак = Літак("Boeing 747", 900, 10000)
    велосипед = Велосипед("Гірський велосипед", 25, "гірський")
    
    транспорти = [авто, літак, велосипед]
    
    for транспорт in транспорти:
        print(транспорт)
        print(транспорт.переміщення(300))
        print()